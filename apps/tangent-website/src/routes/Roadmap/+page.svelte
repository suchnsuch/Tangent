<article>
	<p>
		Tangent is in active development.
		You can follow its progress on the
		<a href="https://github.com/orgs/suchnsuch/projects/2/" target="_blank">Github project</a>
		or by checking out the active
		<a href="https://github.com/suchnsuch/Tangent/issues" target="_blank">issues</a>.
	</p>
	<p>
		Here is a glimpse into the current long-term plans for the project.
	</p>
	<h1>Soon™</h1>
	<ul>
		<li><strong>Export</strong> notes to html or simplified markdown.</li>
		<li>
			<strong>Line-Level Queries</strong>
			– Search for individual lines & todos in your notes.
		</li>
	</ul>
	<h1>Later</h1>
	<ul>
		<li>
			<strong>Board-Style Productivity</strong>
			– Visualize notes on a free-form board, with optional list, table, and swim lane structures.
		</li>
		<li>
			<strong>Note refactoring controls</strong>
			– Extract the context of notes to new or existing notes with robust automation.
		</li>
		<li>
			<strong>Tag Parameters</strong>
			– Add secondary information to your tags.
		</li>
	</ul>
	<h1>Much Later</h1>
	<ul>
		<li><strong>View websites & PDFs alongside notes</strong> with optional automatic archiving.</li>
		<li><strong>iOS & Android</strong> support.</li>
		<li><strong>Collaborative writing</strong> via shared workspaces.</li>
	</ul>

